
public class Position {
    private int x, y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Description: Gets the x value of a position.
     * Output:      x, the x value.
     * Comment:
     */
    public int getX() {
        return x;
    }

    /**
     * Description: Gets the y value of a position.
     * Output:      y, the y value.
     * Comment:
     */
    public int getY() {
        return y;
    }

}
